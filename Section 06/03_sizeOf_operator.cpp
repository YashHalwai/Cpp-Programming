// sizeof Operator

#include <iostream>
using namespace std;

int main()
{
    int x;
    cout << sizeof(x) << endl;

    cout << "Char = " << sizeof(char) << endl;
    cout << "Boolean = " << sizeof(bool) << endl;
    cout << "Float = " << sizeof(float) << endl;
    cout << "Double = " << sizeof(double) << endl;
}