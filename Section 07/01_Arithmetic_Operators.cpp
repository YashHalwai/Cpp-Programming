// Arithmetic Operators - - > + - * / %

#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 3;

    cout << (a+b) << endl;
    cout << (a-b) << endl;
    cout << (a*b) << endl;
    cout << (a/b) << endl;
    cout << (a%b) << endl;

    cout << "A = " << a << endl;
    cout << "B = " << b << endl;
    
    return 0;
}